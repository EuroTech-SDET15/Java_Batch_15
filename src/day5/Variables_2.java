package day5;

public class Variables_2 {
    public static void main(String[] args) {
        int number = 10;
        int var1 = number;
        System.out.println("var1 = " + var1);
        number = 200;
        System.out.println("var1 = " + var1);
        System.out.println("number = " + number);


        char letter = 'A';
        int value = letter;
        System.out.println("value = " + value);
        value = '!';
        System.out.println("value = " + value);
        int letter2='Z';
        System.out.println("letter2 = " + letter2);

    }
}
